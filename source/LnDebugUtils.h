/*
	动态类实例测漏，在类声明里增加宏：DECLARE_OBJCHECKER(ClassName)
	ClassName是类的名字，比如：
		class MyClass
		{
		private:
			DECLARE_OBJCHECKER(MyClass)
		};
	程序结束时，如果存在该类的实例未释放，则会在Output窗口里打印出来
*/

/*
	类实例计数器，内部使用
*/
class _ObjectCounter
{
public:
	_ObjectCounter(const char* szClass, const char* szFile, int nLine)
		: mRef(0), mClass(szClass), mFile(szFile), mLine(nLine) 
	{
	}

	~_ObjectCounter()
	{ 
		if (mRef > 0)
			LN_TRACE(L"object leak:\n\tclass: %s\n\tinstance count: %d\n\tfile: %s\n\tline: %d\n", 
			mClass, mRef, mFile, mLine);
	}

	void operator++()	
	{ 
		++mRef; 
	}

	void operator--()	
	{ 
		--mRef; 
	}
public:
	unsigned	mRef;
	const char* mClass;
	const char* mFile;
	int			mLine;
};

